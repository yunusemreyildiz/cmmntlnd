def _export_review_row(review: Dict) -> List:
    """CSV satırı için alanları normalize et."""
    date_val = review.get('date', '')
    if isinstance(date_val, datetime):
        date_val = date_val.strftime('%Y-%m-%d %H:%M:%S')
    platform = review.get('platform') or (
        'iOS' if review.get('source') == 'App Store' else 'Android'
    )
    return [
        platform,
        review.get('source', ''),
        review.get('review_id', ''),
        review.get('rating', ''),
        review.get('title', ''),
        review.get('content', ''),
        review.get('author', ''),
        date_val,
        review.get('version', ''),
        review.get('url', ''),
        review.get('country', ''),
    ]


@app.route('/export_reviews', methods=['POST'])
def export_reviews():
    """Geçmiş yorumları export et"""
    try:
        time_period = request.form.get('time_period')
        export_format = request.form.get('export_format', 'csv')
        platforms = request.form.getlist('platforms')
        if not platforms:
            platforms = ['app_store', 'google_play']

        from datetime import timedelta
        from dotenv import load_dotenv
        from config import Config
        from app_review_monitor import AppReviewMonitor
        import csv
        import io
        import json
        from flask import make_response

        load_dotenv(dotenv_path=ENV_PATH, override=True)
        Config.reload_from_env()

        if time_period == '15_days':
            days = 15
        elif time_period == '1_month':
            days = 30
        elif time_period == '2_months':
            days = 60
        elif time_period == '3_months':
            days = 90
        elif time_period == '6_months':
            days = 180
        elif time_period == '1_year':
            days = 365
        else:
            return jsonify({'success': False, 'message': 'Geçersiz zaman aralığı'}), 400

        start_date = datetime.now() - timedelta(days=days)
        add_log(f"Export başladı: {time_period}, platformlar={platforms}", 'info')

        monitor = AppReviewMonitor()

        # Önce DB'den oku (anında); aralıkta kayıt yoksa API'den çekip DB'ye yaz
        historical_reviews = monitor.db.get_reviews_since(start_date, platforms=platforms)
        if not historical_reviews:
            add_log("DB'de kayıt yok — API'den çekiliyor ve DB'ye yazılıyor...", 'info')
            fetched = monitor.get_historical_reviews(start_date, platforms=platforms)
            for review in fetched:
                # Geçmiş arşiv; Slack'e basılmasın diye gönderilmiş işaretle
                monitor.db.upsert_review(review, sent=True)
            historical_reviews = monitor.db.get_reviews_since(start_date, platforms=platforms)

        ios_count = sum(1 for r in historical_reviews if r.get('source') == 'App Store')
        android_count = sum(1 for r in historical_reviews if r.get('source') == 'Google Play')
        add_log(
            f"Export veri: toplam={len(historical_reviews)}, iOS={ios_count}, Android={android_count}",
            'info',
        )

        stamp = datetime.now().strftime('%Y%m%d')

        if export_format == 'csv':
            output = io.StringIO()
            writer = csv.writer(output)
            writer.writerow([
                'Platform', 'Store', 'Review ID', 'Rating', 'Title', 'Content',
                'Author', 'Date', 'Version', 'URL', 'Country',
            ])
            for review in historical_reviews:
                writer.writerow(_export_review_row(review))

            response = make_response('\ufeff' + output.getvalue())
            response.headers['Content-Type'] = 'text/csv; charset=utf-8'
            response.headers['Content-Disposition'] = (
                f'attachment; filename=app_reviews_{time_period}_{stamp}.csv'
            )
            add_log(f"CSV export tamamlandı: {len(historical_reviews)} yorum", 'success')
            return response

        if export_format == 'json':
            payload = []
            for review in historical_reviews:
                row = dict(zip(
                    ['platform', 'store', 'review_id', 'rating', 'title', 'content',
                     'author', 'date', 'version', 'url', 'country'],
                    _export_review_row(review),
                ))
                payload.append(row)
            response = make_response(json.dumps(payload, ensure_ascii=False, indent=2))
            response.headers['Content-Type'] = 'application/json; charset=utf-8'
            response.headers['Content-Disposition'] = (
                f'attachment; filename=app_reviews_{time_period}_{stamp}.json'
            )
            add_log(f"JSON export tamamlandı: {len(historical_reviews)} yorum", 'success')
            return response

        return jsonify({'success': False, 'message': 'Desteklenmeyen format'}), 400

    except Exception as e:
        add_log(f"Export hatası: {str(e)}", 'error')
        return jsonify({'success': False, 'message': f'Hata: {str(e)}'}), 500

