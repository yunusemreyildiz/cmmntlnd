@app.route('/start_monitor', methods=['POST'])
def start_monitor():
    """Monitor'u başlat"""
    global monitor_thread, monitor_running

    with monitor_start_lock:
        if monitor_running and monitor_thread and monitor_thread.is_alive():
            return jsonify({'success': False, 'message': 'Monitor zaten çalışıyor'})

        try:
            # Race condition engeli: önce running flag'i aç, sonra thread başlat
            monitor_running = True
            monitor_thread = threading.Thread(target=run_monitor)
            monitor_thread.daemon = True
            monitor_thread.start()
            add_log("Monitor başlatıldı", 'success')
            return jsonify({'success': True, 'message': 'Monitor başlatıldı!'})
        except Exception as e:
            monitor_running = False
            add_log(f"Monitor başlatma hatası: {str(e)}", 'error')
            return jsonify({'success': False, 'message': f'Hata: {str(e)}'})

