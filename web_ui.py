def _maybe_send_daily_summary(monitor, target_time: str) -> None:
    """Hedef saat geçmiş ve bugün özet gönderilmemişse günlük özeti gönder.

    'Bugün gönderildi mi' kontrolü DB'deki app_stats kaydından yapılır (idempotent).
    """
    now = datetime.now()
    today = now.strftime('%Y-%m-%d')

    try:
        hour, minute = (int(x) for x in target_time.split(':'))
    except (ValueError, AttributeError):
        hour, minute = 10, 0

    # Hedef saate ulaşılmadıysa bekle
    if (now.hour, now.minute) < (hour, minute):
        return

    # Bugün zaten bir snapshot alındıysa (özet gönderildiyse) tekrar gönderme
    latest = monitor.db.get_latest_stats('iOS')
    if latest and latest.get('captured_date') == today:
        return

    add_log(f"Günlük özet gönderiliyor ({target_time})...", 'info')
    if monitor.send_daily_summary():
        add_log("Günlük özet Slack'e gönderildi", 'success')
    else:
        add_log("Günlük özet gönderilemedi", 'error')


def run_monitor():
    """Monitor'u çalıştır (thread içinde)"""
    global monitor_running, monitor_lock_fd

    try:
        # Aynı anda ikinci monitor sürecini engelle (process-level lock)
        monitor_lock_fd = open(MONITOR_LOCK_PATH, 'w')
        fcntl.flock(monitor_lock_fd.fileno(), fcntl.LOCK_EX | fcntl.LOCK_NB)

        # .env dosyasını yeniden yükle
        add_log("Monitor thread başlatılıyor, ayarlar yükleniyor...", 'info')
        from dotenv import load_dotenv
        load_dotenv(dotenv_path=ENV_PATH, override=True)  # .env dosyasını tekrar yükle

        from app_review_monitor import AppReviewMonitor
        
        # SADECE BİR KEZ AppReviewMonitor oluştur
        monitor = AppReviewMonitor()
        add_log(f"Monitor hazır - Slack Webhook: {monitor.config.SLACK_WEBHOOK_URL[:50]}...", 'info')
        add_log(f"Monitor sent_reviews boyutu: {len(monitor.sent_reviews)}", 'info')

        daily_summary_time = os.getenv('DAILY_SUMMARY_TIME', '10:00')
        
        while monitor_running:
            try:
                add_log("Yorum kontrol süreci başlatılıyor...", 'info')
                
                # Disk ile birleştir (= kullanma: disk yazılamazsa boş set belleği sıfırlar, aynı yorumlar saatte bir tekrar gider)
                disk_ids = monitor.load_sent_reviews()
                monitor.sent_reviews |= disk_ids
                add_log(f"Sent reviews birleştirildi (bellek ∪ disk): {len(monitor.sent_reviews)} adet", 'info')
                
                monitor.check_and_send_reviews()
                add_log("Yorum kontrolü tamamlandı", 'success')

                # Günlük özet: hedef saat geçtiyse ve bugün henüz gönderilmediyse gönder
                try:
                    _maybe_send_daily_summary(monitor, daily_summary_time)
                except Exception as e:
                    add_log(f"Günlük özet kontrolü hatası: {e}", 'error')
                
                # Bekleme süresi - daha kısa aralıklarla kontrol et
                for _ in range(monitor.config.CHECK_INTERVAL_MINUTES * 60):
                    if not monitor_running:
                        break
                    time.sleep(1)
                    
            except Exception as e:
                add_log(f"Monitor hatası: {str(e)}", 'error')
                time.sleep(60)  # Hata durumunda 1 dakika bekle
                
    except BlockingIOError:
        add_log("Monitor zaten başka bir süreçte çalışıyor, ikinci kez başlatılmadı", 'warning')
        monitor_running = False
    except PermissionError as e:
        add_log(f"Monitor başlatma hatası: .env erişim izni yok ({ENV_PATH}) - {str(e)}", 'error')
        monitor_running = False
    except Exception as e:
        add_log(f"Monitor başlatma hatası: {str(e)}", 'error')
        monitor_running = False
    finally:
        if monitor_lock_fd:
            try:
                fcntl.flock(monitor_lock_fd.fileno(), fcntl.LOCK_UN)
                monitor_lock_fd.close()
            except Exception:
                pass
            monitor_lock_fd = None

        # Sadece thread sonlandığında monitor_running'i false yap
        if monitor_running:
            add_log("Monitor thread sonlandırıldı", 'info')
        monitor_running = False

def _acquire_web_ui_singleton_lock() -> None:
    """Bu proje dizininde tek Web UI süreci (ALLOW_MULTIPLE_WEB_UI=1 ile kapatılabilir)."""
    global _web_ui_singleton_fd
    if os.environ.get('ALLOW_MULTIPLE_WEB_UI', '').strip() == '1':
        return
    fd = open(WEB_UI_SINGLETON_LOCK_PATH, 'w')
    try:
        fcntl.flock(fd.fileno(), fcntl.LOCK_EX | fcntl.LOCK_NB)
    except BlockingIOError:
        fd.close()
        try:
            with open(WEB_UI_SINGLETON_LOCK_PATH, 'r', encoding='utf-8') as rf:
                other = rf.read().strip() or '?'
        except OSError:
            other = '?'
        print(
            f"❌ Bu proje için Web UI zaten çalışıyor (.web_ui.lock, diğer PID/port: {other}).\n"
            f"   İkinci süreç Slack'te çift bildirim üretebilir. Önce eski süreci durdur.\n"
            f"   Geliştirici modunda çok süreç istiyorsan: ALLOW_MULTIPLE_WEB_UI=1 python web_ui.py ...",
            file=sys.stderr,
        )
        raise SystemExit(1) from None
    fd.write(f"pid={os.getpid()}\n")
    fd.flush()
    _web_ui_singleton_fd = fd


if __name__ == '__main__':
    _acquire_web_ui_singleton_lock()

    # Port parametresi kontrol et
    port = 5000
    if len(sys.argv) > 1 and sys.argv[1] == '--port':
        try:
            port = int(sys.argv[2])
        except (IndexError, ValueError):
            port = 5000

    if _web_ui_singleton_fd is not None:
        _web_ui_singleton_fd.write(f"port={port}\n")
        _web_ui_singleton_fd.flush()

    add_log(f"Web UI başlatıldı - Port: {port} (PID {os.getpid()})", 'info')
    app.run(debug=False, use_reloader=False, host='0.0.0.0', port=port)
